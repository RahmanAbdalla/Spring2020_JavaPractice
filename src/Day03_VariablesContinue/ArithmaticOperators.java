package Day03_VariablesContinue;

public class ArithmaticOperators {
    public static void main(String[] args) {

        // +, -, *, /, %

        System.out.println(5+3); //8, this is addition
        System.out.println(10-3); // 7, this is subtraction
        System.out.println(10*2); // 20, this is multiplication
        System.out.println(10/2); // 5, this is division

        // System.out.println(9 / 0); //This gives error coz in division the denominator cannot be ZERO

        System.out.println("The remainder of 10/3 is: "+ 10 % 3); // result is: 1
        System.out.println("The remainder of 100/33 is: " + 100 % 33); // result is: 1


    }

}
