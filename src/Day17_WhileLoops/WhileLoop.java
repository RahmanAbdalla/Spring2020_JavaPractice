package Day17_WhileLoops;

public class WhileLoop {
    public static void main(String[] args) {

        int i=0;
        while (i <= 5){
            System.out.println(i);//this prints form 0-5
            i++;// it makes different if we put it before or after the print statement
        }

        System.out.println("=======================================");
        int n=0;
        while (n <= 5){
            n++;
            System.out.println(n); //this prints form 1-6
        }

    }
}
