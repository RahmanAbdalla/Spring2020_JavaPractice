package Day02_Variables;

public class escapeSequences {
    public static void main(String[] args) {
        System.out.println("Hello World");
        System.out.println("\tHello World");
        // \t gives spaces to the paragraph
        System.out.println("One Two \nThree");
        System.out.println("\n\n\n Hello World");
        // \n prints statement in a new line
        // My favorite TV show is "Game of Throne"
        System.out.println("My favorite TV show is \"Game of Throne\"");
        // \" or \' is used to put sth inside a double or single quote
        System.out.println("\\");

    }
}
